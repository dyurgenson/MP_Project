namespace YA_Metro.Utilities.Logger
{
    /// <summary>
    /// Перечисление, представляющее уровни серьезности записей лога.
    /// </summary>
    public enum Severity
    {
        /// <summary>
        /// Информационное сообщение.
        /// </summary>
        Info,

        /// <summary>
        /// Предупреждение.
        /// </summary>
        Warning,

        /// <summary>
        /// Критическая ошибка.
        /// </summary>
        Critical,
    }
}